class Solution
{
    public:
    //Function to find the first non-repeating character in a string.
    char nonrepeatingCharacter(string S)
    {
        int arr[27]={0};
        for(auto it: S) arr[it-'a']++;
        for(auto it: S) if(arr[it-'a']==1) return it;
        return '$';
       //Your code here
       
    }

};

